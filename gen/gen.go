//go:build ignore

package main

import (
	"bytes"
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"sort"
	"strings"
	"text/template"
)

func main() {
	// go:generate runs with the package directory as CWD.
	// go run gen/gen.go is also invoked from the package root.
	genDir, _ := filepath.Abs("gen")
	outDir, _ := filepath.Abs(".")

	bin, err := os.CreateTemp("", "reqrep_gen_layout_*")
	if err != nil {
		fatalf("tempfile: %v", err)
	}
	bin.Close()
	defer os.Remove(bin.Name())

	cmd := exec.Command("gcc",
		"-o", bin.Name(),
		"-D_GNU_SOURCE",
		filepath.Join(genDir, "gen_layout.c"),
		"-I", genDir,
	)
	cmd.Stderr = os.Stderr
	if err := cmd.Run(); err != nil {
		fatalf("gcc: %v", err)
	}

	out, err := exec.Command(bin.Name()).Output()
	if err != nil {
		fatalf("gen binary: %v", err)
	}

	consts := map[string]string{}
	for _, line := range strings.Split(string(out), "\n") {
		line = strings.TrimSpace(line)
		if line == "" {
			continue
		}
		parts := strings.SplitN(line, " = ", 2)
		if len(parts) == 2 {
			consts[strings.TrimSpace(parts[0])] = strings.TrimSpace(parts[1])
		}
	}

	// Sort keys for deterministic output
	keys := make([]string, 0, len(consts))
	for k := range consts {
		keys = append(keys, k)
	}
	sort.Strings(keys)

	type kv struct{ K, V string }
	pairs := make([]kv, len(keys))
	for i, k := range keys {
		pairs[i] = kv{k, consts[k]}
	}

	tmpl := template.Must(template.New("").Parse(goTemplate))
	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, pairs); err != nil {
		fatalf("template: %v", err)
	}

	outFile := filepath.Join(outDir, "reqrep_layout_gen.go")
	if err := os.WriteFile(outFile, buf.Bytes(), 0644); err != nil {
		fatalf("write %s: %v", outFile, err)
	}
	fmt.Println("generated", outFile)
}

func fatalf(format string, args ...any) {
	fmt.Fprintf(os.Stderr, "gen: "+format+"\n", args...)
	os.Exit(1)
}

const goTemplate = `// Code generated by gen/gen.go from gen/reqrep.h. DO NOT EDIT.

package reqrep

import "unsafe"

const (
{{- range .}}
	layout{{.K}} = {{.V}}
{{- end}}
)

// hdrField32 and hdrField64 are inlined by the compiler; no bounds check.
func hdrField32(base unsafe.Pointer, off uintptr) *uint32 {
	return (*uint32)(unsafe.Pointer(uintptr(base) + off))
}

func hdrField64(base unsafe.Pointer, off uintptr) *uint64 {
	return (*uint64)(unsafe.Pointer(uintptr(base) + off))
}
`
